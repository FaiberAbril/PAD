<?php

namespace pad\Http\Controllers;

use Illuminate\Http\Request;
use Session;
use Redirect;
use pad\Http\Requests;
use pad\Http\Controllers\Controller;
use pad\Grupos;

class GruposController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $grupos = Grupos::All();
        return view('grupos.index',compact('grupos'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('grupos.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Grupos::create([
                'nombre_grupo' => $request['nombre_grupo'],
                'codigo_grupo' => $request['codigo_grupo']
            ]);
        return redirect('/grupos')->with('message','Grupo Creado');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $grupos = Grupos::find($id);
        return view('grupos.edit',['grupos'=>$grupos]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $grupos = Grupos::find($id);
        $grupos->fill($request->all());
        $grupos->save();
        Session::flash('message','Grupo Editado');
        return Redirect::to('/grupos');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
    }
}
